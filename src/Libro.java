public class Libro {

    private String titulo;
    private String autor;
    private int añoPublicacion;

    private int año;

    public String getTitulo() {
        return titulo;
    }

    public String getAutor() {
        return autor;
    }

    public int getAñoPublicacion() {
        return añoPublicacion;
    }

    public int getAño() {
        return año;
    }
}
